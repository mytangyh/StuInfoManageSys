    private void jLabel5AncestorAdded(javax.swing.event.AncestorEvent evt) {//GEN-FIRST:event_jLabel5AncestorAdded
        // TODO add your handling code here:
        new Thread() {
            @Override
            public void run() {

                while (true) {
                    try {
                        Thread.sleep(300);

                        String pass1 = new String(passwd1.getPassword());
                        String pass2 = new String(passwd2.getPassword());
                        if (pass1.length() > 6 && pass2.length() > 0) {
                            if (!pass1.equals(pass2)) {
                                jLabel5.setText("两次密码不一致");
                            } else {
                                jLabel5.setText("OK!");
                            }
                        }
                        if (pass1.length() > 0 && pass1.length() <= 6) {
                            jLabel4.setText("*长度必须大于6");
                        }
                        if (pass1.length() > 6) {
                            jLabel4.setText("OK!");
                        }

                    } catch (InterruptedException ex) {
                        Logger.getLogger(Stu_AlterPass.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }

        }.start();

    }//GEN-LAST:event_jLabel5AncestorAdded


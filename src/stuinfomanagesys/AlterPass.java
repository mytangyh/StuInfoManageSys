    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String admin = jTextField1.getText().trim();
        String pass = new String(passwd1.getPassword());
        Connection con = MysqlConn.getCon();
        String sqladmin = "update admin_user set username = '" + admin + "'where username = '" + this.admin + "'";
        String sqlpass = "update admin_user set password = '" + pass + "'where username = '" + this.admin + "'";
        try {
            Statement stat = con.createStatement();
            Statement stat1 = con.createStatement();
            stat1.executeUpdate(sqlpass);
            stat.executeUpdate(sqladmin);
        } catch (SQLException ex) {
            Logger.getLogger(AlterPass.class.getName()).log(Level.SEVERE, null, ex);
        }

        JOptionPane.showMessageDialog(null, "修改成功，请重新登录");
        AdminLogin adminLogin = new AdminLogin();
        adminLogin.setVisible(true);
        this.dispose();
        this.parent.dispose();

    }//GEN-LAST:event_jButton1ActionPerformed

